/**
 * Supervisor Milestones Scripts
 * - DataTable initialization for milestones table
 */
$(document).ready(function() {
    const table = $('#milestonesTable').DataTable({
        pageLength: 10,
        order: [[ 3, "asc" ]], // Sort by due date ascending
        // Custom DOM layout: Right-aligned filter & search with wrap support for mobile
        dom: '<"d-flex flex-column flex-sm-row justify-content-end align-items-stretch align-items-sm-center gap-2 mb-4 w-100"f>rt<"d-flex flex-column flex-md-row justify-content-between align-items-center mt-3"ip>',
        language: {
            search: "_INPUT_",
            searchPlaceholder: "Search milestones...",
            paginate: { previous: '‹', next: '›' },
            emptyTable: "No milestones have been assigned yet."
        },
        initComplete: function () {
            // Column 1 is "Group"
            const column = this.api().column(1);
            
            // Create the select dropdown with premium styling and w-auto to prevent stretching
            const select = $('<select class="form-select border-secondary-subtle rounded-pill px-15 w-auto" style="min-width: 200px; font-size: 0.875rem;"><option value="">All Groups</option></select>')
                .on('change', function () {
                    const val = $.fn.dataTable.util.escapeRegex($(this).val());
                    column.search(val ? '^' + val + '$' : '', true, false).draw();
                });

            // Extract unique values
            const uniqueValues = new Set();
            column.data().each(function (d) {
                const text = $('<div>').html(d).text().replace(/\s+/g, ' ').trim();
                if (text && text !== 'N/A-N/A') {
                    uniqueValues.add(text);
                }
            });

            // Add sorted options
            Array.from(uniqueValues).sort().forEach(function (text) {
                select.append('<option value="' + text + '">' + text + '</option>');
            });

            // Prepend dropdown to the flex wrapper of dataTables_filter
            $('.dataTables_filter').parent().prepend(select);
        }
    });

    // Refine the Search Input styling
    $('.dataTables_filter label').contents().filter(function() {
        return this.nodeType === 3; // Remove the text "Search:" generated by DataTables
    }).remove();
    
    $('.dataTables_filter input')
        .addClass('form-control border-secondary-subtle rounded-pill px-4')
        .css({
            'min-width': '220px', 
            'margin-left': '0',
            'font-size': '0.875rem'
        });
});
